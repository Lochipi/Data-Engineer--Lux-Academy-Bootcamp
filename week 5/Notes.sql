-- Interview Questions
1). First, understand the most basic SQL Commands, these include:
SELECT - extracts data from a database
UPDATE - updates data in a database
DELETE - deletes data from a database
INSERT INTO - inserts new data into a database
CREATE DATABASE - creates a new database
ALTER DATABASE - modifies a database
CREATE TABLE - creates a new table
ALTER TABLE - modifies a table
DROP TABLE - deletes a table
CREATE INDEX - creates an index (search key)
DROP INDEX - deletes an index

2). The move to advanced concepts, you can use the questions and answers below to practice:
Question: What are the different types of joins in SQL?
Answer: There are four types of joins in SQL: inner join, left outer join, right outer join, and full outer join.

Question: What is a subquery in SQL?
Answer:  A subquery is a query within another query. It is used to retrieve data from one table based on a condition from another table.

Question: What is the difference between UNION and UNION ALL in SQL?
Answer:  UNION removes duplicate rows from the result set, whereas UNION ALL does not.

Question: How do you optimize a SQL query?
Answer: There are several ways to optimize a SQL query, such as:
Use indexes on tables
Reduce the number of joins
Use subqueries instead of joins
Use the EXPLAIN command to analyze the query plan.

Question: What is a stored procedure in SQL?
Answer:  A stored procedure is a set of SQL statements that are stored in the database and can be executed later. It can be used to perform complex operations or tasks and can be called from other programs or applications.

Question: What is a trigger in SQL?
Answer: A trigger is a special type of stored procedure that is automatically executed when a certain event occurs, such as an insert, update, or delete operation on a table.

Question: What is a transaction in SQL?
 Answer: A transaction is a set of SQL statements that are executed as a single unit of work. It ensures that either all the statements in the transaction are executed successfully, or none of them are.

Question: What is the difference between a primary key and a foreign key in SQL?
Answer: A primary key is a column or a set of columns that uniquely identifies each row in a table. A foreign key is a column or a set of columns that refer to the primary key of another table, creating a relationship between the two tables.

Question: What is normalization in SQL?
Answer: Normalization is the process of organizing data in a database by breaking down larger tables into smaller, more specific tables, to eliminate redundancy and improve data integrity.

Q: What is an index in SQL?
 Answer: An index is a database structure that is used to improve the performance of queries by allowing them to quickly locate data within a table. It works by creating a separate data structure that contains a subset of the data in the table, organized in a way that makes it easier and faster to search.

